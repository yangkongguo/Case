package towaynet.entity;


public class FlushNum {
	private String nowDate;
    private int flush_num ;
    private int case_num;
	
	public String getNowDate() {
		return nowDate;
	}
	public void setNowDate(String nowDate) {
		this.nowDate = nowDate;
	}
	public int getFlush_num() {
		return flush_num;
	}
	public void setFlush_num(int flush_num) {
		this.flush_num = flush_num;
	}
	public int getCase_num() {
		return case_num;
	}
	public void setCase_num(int case_num) {
		this.case_num = case_num;
	}
    
}
